<h2 id="script-标签"><a class="header-anchor" href="#script-标签">#</a> script 标签</h2>
<ol>
<li>async：表示立即开始下载脚本，但不能阻止其它页面动作，比如下载资源或等待其它脚本加载。只对外部脚本文件有效。</li>
<li>charset：字符集</li>
<li>crossorigin：CORS（跨源资源共享）设置，默认不使用CORS。</li>
<li>defer：脚本可以延迟到文档完全被解析和显示后再执行。外部脚本有效。</li>
<li>integrity：接收到的资源和指定的加密签名验证。</li>
<li>language：废弃。</li>
<li>src：外部文件</li>
<li>type：代替language，表示脚本的语言类型。text/javascript。</li>
</ol>
